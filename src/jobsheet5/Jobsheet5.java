public class Jobsheet5 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        /**
     * @param args the command line arguments
     */
    
        int a = 10;
        int b = 2;
        
        int c;
        
        //Penjumlahan
        c = a + b;
        System.out.println(a + " + " + b + " = " + c);
        
        //Pengurangan
        c = a - b;
        System.out.println(a + " - " + b + " = " + c);
        
        //Perkalian
        c = a * b;
        System.out.println(a + " X " + b + " = " + c);
        
        //Pembagian
        c = a / b;
        System.out.println(a + " : " + b + " = " + c);
        
        //Sisa pembagian
        c = a % b;
        System.out.println(a + " % " + b + " = " + c);
        
    }
    
    
}
